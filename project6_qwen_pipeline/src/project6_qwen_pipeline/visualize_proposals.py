    import argparse
    
    OUTPUT_DIR = "/rhome/jankula/ML3D_project/project6_qwen_pipeline/src/outputs"
    SNAPSHOT_DIR = "/rhome/jankula/ML3D_project/project6_qwen_pipeline/src/project6_qwen_pipeline/rendered_snapshots"
    

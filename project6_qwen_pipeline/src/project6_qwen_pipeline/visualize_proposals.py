    parser = argparse.ArgumentParser(description="Render specific Qwen JSON layouts.")
    parser.add_argument(
        "--file", 
        type=str, 
        default=None, 
        help="Specific JSON filename to render (e.g. layout_sofa.json). If omitted, renders the newest file."
    )
    args = parser.parse_args()

    # 2. Initialize visualizer class
    visualizer = QwenLayoutVisualizer(OUTPUT_DIR, SNAPSHOT_DIR)
    
    # 3. Choose execution path based on input
    if args.file:
        # User specified a file
        target_path = Path(OUTPUT_DIR) / args.file
        if not target_path.exists():
            print(f"❌ Error: The file '{args.file}' does not exist inside {OUTPUT_DIR}")
            print("Available files:")
            for f in sorted(list(Path(OUTPUT_DIR).glob("*.json"))):
                print(f" - {f.name}")
        else:
            visualizer.render_layout(target_path)
    else:
        # No file specified -> Automatically grab the absolute newest creation!
        all_json_files = list(Path(OUTPUT_DIR).glob("*.json"))
        if not all_json_files:
            print("❌ No JSON output layouts found.")
        else:
            # Sort files by their file modification time (most recent first)
            all_json_files.sort(key=lambda x: x.stat().st_mtime, reverse=True)
            newest_file = all_json_files[0]
            
            print(f"💡 No file specified. Automatically rendering the most recent layout creation...")
            visualizer.render_layout(newest_file)